# Accessing Values (elements)
color = (53, 33, 0)

r = color[0]
g = color[1]
b = color[2]

print(r, g, b)

matthews = ["Matthew 1", "Matthew 2", "Matthew 3"]

print(matthews[0])
print(matthews[1])
print(matthews[2])

matthews[0] = "Poon"
matthews[-1] = "Ho"

# Len()
color = (53, 33, 0)
matthews = ["Matthew 1", "Matthew 2", "Matthew 3", "Matthew 4", "Hahhaha"]

print(len(color))
print(len(matthews))

# Slice
teachers = ["Poon", "Peters", "Vi", "Yasui"]

print(teachers)
print(teachers[0])
print(teachers[0:3])
print(teachers[1:3])
